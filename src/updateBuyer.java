    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String caseno = jTextField1.getText();
        try {
            Connection c = ConnectionProvider.getConnection();
            Statement st= c.createStatement();
            ResultSet rs= st.executeQuery("SELECT * FROM PATIENT WHERE CASENO = '"+caseno+"'");
            rs.next();
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(5));
            jTextField4.setText(rs.getString(3));
            jTextField5.setText(rs.getString(6));
            int t;
            if (rs.getString(4).equals("Female")) {
                t = 0;
            }
            else if (rs.getString(4).equals("Male")) {
                t = 1;
            }
            else {
                t = 2;
            }
            jComboBox1.setSelectedIndex(t);
            jTextField1.setEditable(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Unable to find contact no.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


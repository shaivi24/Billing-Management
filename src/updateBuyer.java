    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String caseno = jTextField1.getText();
        String name=jTextField2.getText();
        String contact =jTextField3.getText();
        String age = jTextField4.getText();
        String address = jTextField5.getText();
        String gender;
        int i = jComboBox1.getSelectedIndex();
        if (i==0) gender = "Female";
        else if (i==1) gender = "Male";
        else gender = "Other";
        Connection con= ConnectionProvider.getConnection();
        try {
            PreparedStatement st = con.prepareStatement("UPDATE PATIENT SET NAME=?, CONTACTNO=?, AGE=?, ADDRESS=?, GENDER=? WHERE CaseNO='" + caseno+"';");
            st.setString(1, name);
            st.setString(2,contact);
            st.setString(3,age);
            st.setString(4,address);
            st.setString(5,gender);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Patient Updated Succesfully");
            setVisible(false);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

